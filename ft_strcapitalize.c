char	*ft_strcapitalize(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	while (str[i] != '\0')
	{
		if (((str[i] >= 65) && (str[i] <= 90))
			|| ((str[i] >= 97) && (str[i] <= 122))
			|| ((str[i] >= 48) && (str[i] <= 57)))
		{
			if ((str[i] >= 65) && (str[i] <= 90) && (a == 0))
				str[i] = str[i] + 32;
			else if ((str[i] >= 97) && (str[i] <= 122) && (a == 1))
				str[i] = str[i] - 32;
			a = 0;
		}
		else
			a = 1;
		i++;
	}
	return (str);
}
